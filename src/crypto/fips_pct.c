{
    MSTATUS status  = OK;
    ubyte4 verifyStatus  = 0;
    edECCKey* pNewKey = NULL;
    BulkHashAlgo shaSuite = {0};
    ubyte pSignature[57*2] = {0}; /* big enough for either curve */
    ubyte4 signatureLen;

    const ubyte mesg[] =
    {
      0xD1,0x1C,0xA5,0xB4,0xC0,0xE6,0x10,0xD8,0x4B,0x14,0xCF,0xED,0x38,0xFF,0x64,0x58
    };

    ubyte4 mesgLen = 16;

    status = CURVE25519_createCombMutex();
    if (OK != status)
    {
         goto exit;
    }

    status = CURVE448_createCombMutex();
    if (OK != status)
    {
         goto exit;
    }

    status = edECC_newKey(&pNewKey, curveEd25519, NULL);
    if (OK != status)
        goto exit;

    if (OK > (status = FIPS_getEDDSAKey25519(pNewKey)))
        goto exit;

    shaSuite.allocFunc = &SHA512_allocDigest;
    shaSuite.initFunc = (BulkCtxInitFunc) &SHA512_initDigest;
    shaSuite.updateFunc = (BulkCtxUpdateFunc) &SHA512_updateDigest;
    shaSuite.finalFunc = (BulkCtxFinalFunc) &SHA512_finalDigest;
    shaSuite.freeFunc = &SHA512_freeDigest;
    shaSuite.digestFunc = NULL;

    status = edDSA_Sign(pNewKey, (ubyte *) mesg, mesgLen, pSignature,
        sizeof(pSignature), &signatureLen, &shaSuite, FALSE, NULL, 0, NULL);
    if (OK != status)
    {
         goto exit;
    }

    if (FIPS_FORCE_FAIL_EDDSA_TEST)
    {
        pSignature[0] ^= 0x783F;
    }

    pNewKey->isPrivate = FALSE;
    status = edDSA_VerifySignature(pNewKey, mesg, mesgLen, pSignature,
        signatureLen, &verifyStatus, &shaSuite, FALSE, NULL, 0, NULL);
    if (OK != verifyStatus)
    {
         status = ERR_FALSE;
         goto exit;
    }

    /* do a negative test */
    status= edDSA_VerifySignature(pNewKey, mesg, mesgLen - 1, pSignature,
        signatureLen, &verifyStatus, &shaSuite, FALSE, NULL, 0, NULL);
    if(1 != verifyStatus)
    {
        status = ERR_CRYPTO;
        goto exit;
    }
    status = OK;

exit:
    if (NULL != pNewKey)
    {
        edECC_deleteKey(&pNewKey, NULL);
    }

    return status;
}

