static MSTATUS
FIPS_pctEcdsaTest(hwAccelDescr hwAccelCtx, randomContext *pRandomContext)
{
    PFEPtr              r       = NULL;
    PFEPtr              s       = NULL;
    PrimeFieldPtr       pPF     = NULL;
    ECCKey*             pNewKey = NULL;
    MSTATUS             status  = OK;

    const ubyte hash[28] =
    {
      0xFB,0x55,0x3D,0xE7,0x46,0xCE,0x92,0xEB,0x85,0x61,0x08,0xBA,0xAC,0x5C,0x83,0xFA,
      0x7E,0x09,0x0A,0xCD,0xEE,0xA7,0x6C,0xB6,0x01,0xE8,0xCC,0xED
    };
    ubyte4 hashLen = 28;

    if (OK > (status = EC_newKey(EC_P224, &pNewKey)))
        goto exit;

    if (OK > (status = FIPS_getECDSAKey224(pNewKey)))
        goto exit;

    pPF = EC_getUnderlyingField(pNewKey->pCurve);

    if (OK > (status = PRIMEFIELD_newElement(pPF, &r)))
        goto exit;

    if (OK > (status = PRIMEFIELD_newElement(pPF, &s)))
        goto exit;

    if (OK > (status = ECDSA_signDigestAux(pNewKey->pCurve, pNewKey->k, RANDOM_rngFun,
                                  pRandomContext, hash, hashLen, r, s)))
    {
        goto exit;
    }

    if (FIPS_FORCE_FAIL_ECDSA_TEST)
    {
        r->units[0] ^= 0x783F;
    }

    if (OK > (status = ECDSA_verifySignature(pNewKey->pCurve,
                                             pNewKey->Qx, pNewKey->Qy, hash, hashLen,
                                             r, s)))
    {
        goto exit;
    }

    /* do a negative test */
    if (OK <= (status = ECDSA_verifySignature(pNewKey->pCurve,
                                              pNewKey->Qx, pNewKey->Qy, hash, hashLen - 1,
                                              r, s)))
    {
        status = ERR_FIPS_ECDSA_PCT_FAILED;
        goto exit;
    }

    status = OK;

exit:
    PRIMEFIELD_deleteElement(pPF, &r);
    PRIMEFIELD_deleteElement(pPF, &s);

    EC_deleteKey(&pNewKey);
    return status;
}

