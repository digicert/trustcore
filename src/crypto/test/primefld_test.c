int primefld_test_reduction_functions()
{
    int retVal = 0;

#ifdef __ENABLE_DIGICERT_ECC_P192__
    retVal += test_reduction_function( PF_p192, 192);
#endif
    retVal += test_reduction_function( PF_p224, 224);
    retVal += test_reduction_function( PF_p256, 256);
    retVal += test_reduction_function( PF_p384, 384);
    retVal += test_reduction_function( PF_p521, 521);

    return retVal;
}


