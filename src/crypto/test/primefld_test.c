MSTATUS PrimeFldFromStr( const sbyte* str, ubyte4* unitLen, pf_unit** a)
{
    ubyte* bs;
    ubyte4 bsLen;

    bs = 0;
    bsLen = UNITTEST_UTILS_str_to_byteStr(str, &bs);

    *unitLen = (bsLen + sizeof(pf_unit) - 1) / sizeof(pf_unit);

    *a = (pf_unit*) MALLOC( (*unitLen) * sizeof(pf_unit));
    if (! (*a))
    {
        return ERR_MEM_ALLOC_FAIL;
    }
    
    BI_setUnitsToByteString( *unitLen, *a, bs, bsLen);
    FREE( bs);

    return OK;
}

