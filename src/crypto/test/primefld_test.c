int test_reduction_function( PrimeFieldPtr pPF, int hint)
{
    int retVal = 0;
    PFEPtr pA = 0;
    PFEPtr pB = 0;
    PFEPtr pC = 0;
    pf_unit* hilo = 0;

    hilo = MALLOC( pPF->n * 2 * sizeof(pf_unit));
    retVal += UNITTEST_TRUE( hint, hilo != 0); 
    
    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_newElement( pPF, &pA));
    if (retVal) goto exit;
    
    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_newElement( pPF, &pB));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_newElement( pPF, &pC));
    if (retVal) goto exit;

    /* first test is  (p + 1) * 2 === 2 mod p */
    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_copyElement( pPF, pA, (ConstPFEPtr) pPF->units));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_add( pPF, pA, (ConstPFEPtr) g_pOneUnits));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_setToUnsigned( pPF, pB, 2)); 
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_multiplyAux( pPF, pC, pA, pB, hilo));
    if (retVal) goto exit;

    retVal += UNITTEST_INT( hint, PRIMEFIELD_cmp( pPF, pC, pB), 0);

    /* second test is ( (p - 1) * 0xffffffff == -0xffffffff mod p = p -0xffffff ) */ 
    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_copyElement( pPF, pA, (ConstPFEPtr) pPF->units));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_subtract( pPF, pA, (ConstPFEPtr) g_pOneUnits));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_setToUnsigned( pPF, pB, 0xFFFFFFFF));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_multiplyAux( pPF, pC, pA, pB, hilo));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_copyElement( pPF, pA, (ConstPFEPtr) pPF->units));
    if (retVal) goto exit;

    retVal += UNITTEST_STATUS( hint, PRIMEFIELD_subtract( pPF, pA, pB));
    if (retVal) goto exit;

    retVal += UNITTEST_INT( hint, PRIMEFIELD_cmp( pPF, pC, pA), 0);

exit:

    PRIMEFIELD_deleteElement( pPF, &pA);
    PRIMEFIELD_deleteElement( pPF, &pB);
    PRIMEFIELD_deleteElement( pPF, &pC);
    if (hilo) FREE( hilo);

    return retVal;

}


