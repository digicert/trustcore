int primefld_test_match()
{
    int retVal = 0;
    PFEPtr a = 0, b = 0;

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_newElement( PF_p521, &a),
                         retVal, exit);

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_newElement( PF_p521, &b),
                         retVal, exit);

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_setToUnsigned( PF_p521, a, 0xFFFF),
                         retVal, exit);

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_inverse( PF_p521, b, a),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, !PRIMEFIELD_match(PF_p521, a, b));

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_copyElement( PF_p521, a, b),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, PRIMEFIELD_match(PF_p521, a, b));

    UNITTEST_STATUS_GOTO(0, PRIMEFIELD_add(PF_p521, a, (ConstPFEPtr) g_pOneUnits),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, !PRIMEFIELD_match(PF_p521, a, b));

    UNITTEST_STATUS_GOTO(0, PRIMEFIELD_subtract(PF_p521, a, (ConstPFEPtr) g_pOneUnits),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, PRIMEFIELD_match(PF_p521, a, b));

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_inverse( PF_p521, b, a),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, !PRIMEFIELD_match(PF_p521, a, b));

    UNITTEST_STATUS_GOTO( 0, PRIMEFIELD_setToUnsigned( PF_p521, a, 0xFFFF),
                         retVal, exit);

    retVal += UNITTEST_TRUE( 0, PRIMEFIELD_match(PF_p521, a, b));

exit:

    PRIMEFIELD_deleteElement(PF_p521, &a);
    PRIMEFIELD_deleteElement(PF_p521, &b);

    return retVal;
}

