int PrimeFldDivideTest(int hint, const sbyte* a_str, const sbyte* b_str,
                       const sbyte* q_str, const sbyte* r_str)
{
    int i, retVal = 0;
    pf_unit *a, *b, *q, *r, *cq, *cr;
    ubyte4 a_len, b_len, q_len, r_len;

    a = b = q = r = cq = cr = 0;

    retVal += UNITTEST_STATUS(hint, PrimeFldFromStr( a_str, &a_len, &a));
    retVal += UNITTEST_STATUS(hint, PrimeFldFromStr( b_str, &b_len, &b));
    retVal += UNITTEST_STATUS(hint, PrimeFldFromStr( q_str, &q_len, &q));
    retVal += UNITTEST_STATUS(hint, PrimeFldFromStr( r_str, &r_len, &r));
    
    cq = MALLOC( (a_len - b_len + 1) * sizeof(pf_unit));
    cr = MALLOC( b_len * sizeof(pf_unit));
    
    retVal += UNITTEST_TRUE(hint, cq && cr);
    if (retVal)
    {
        goto exit;    
    }

    retVal += UNITTEST_STATUS(hint, BI_divide( a_len, a, b_len, b, cq, cr)); 

    retVal += UNITTEST_TRUE( hint, 0 == BI_cmp( q_len, q, cq));
    retVal += UNITTEST_TRUE( hint, 0 == BI_cmp( r_len, r, cr));

    /* verify the higher digits not in cq or cr are zero */
    for ( i = q_len; i < a_len - b_len + 1; i++)
    {
        retVal += UNITTEST_TRUE(hint, cq[i] == 0);
    }
    for ( i = r_len; i < b_len; i++)
    {
        retVal += UNITTEST_TRUE(hint, cr[i] == 0);
    }
    
exit:

    if (a) FREE(a);
    if (b) FREE(b);
    if (q) FREE(q);
    if (r) FREE(r);
    if (cq) FREE(cq);
    if (cr) FREE(cr);

    return retVal;
}

