extern MSTATUS
SRP_getGroupParameters( ubyte4 bitNum, vlong** modulus, vlong** generator,
                       vlong** srpk)
{
    MSTATUS status = OK;
    const ubyte* modulusBytes;
    const ubyte* generatorBytes;
    const ubyte* kBytes;
    sbyte4 modulusLen, genLen;
    vlong* n = 0;
    vlong* g = 0;
    vlong* k = 0;

    if (OK > ( status = SRP_getGroupParameters2(bitNum,
                                                &modulusBytes, &modulusLen,
                                                &generatorBytes, &genLen,
                                                &kBytes)))
    {
        goto exit;
    }

    if (OK > (status = VLONG_vlongFromByteString(modulusBytes, modulusLen,
                                                 &n, 0)))
    {
        goto exit;
    }

    if (OK > (status = VLONG_vlongFromByteString(generatorBytes, genLen,
                                                 &g, 0)))
    {
        goto exit;
    }

    if (OK > (status = VLONG_vlongFromByteString(kBytes, SHA1_RESULT_SIZE,
                                                 &k, 0)))
    {
        goto exit;
    }

    *modulus = n; n = 0;
    *generator = g; g = 0;
    *srpk = k; k = 0;
exit:

    VLONG_freeVlong(&k, 0);
    VLONG_freeVlong(&g, 0);
    VLONG_freeVlong(&n, 0);
    return status;
}



