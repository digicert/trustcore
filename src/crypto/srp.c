extern MSTATUS
SRP_getGroupParameters2( ubyte4 bitNum, const ubyte** modulus, sbyte4* modulusLen,
                        const ubyte** generator, sbyte4* generatorLen,
                        const ubyte** k)
{
    MSTATUS status = OK;
    static const ubyte kGenerators[] =
    {
        2, 5, 19
    };


    if (bitNum < __DIGICERT_MIN_SRP_BITS__)
    {
        status = ERR_INVALID_ARG;
        goto exit;
    }

    switch(bitNum)
    {
#if __DIGICERT_MIN_SRP_BITS__ <= 1024
        case 1024: /* 2 */
            *k = srp_1024_k;
            *generator = kGenerators;
            *modulus = srp_1024;
            *modulusLen = sizeof(srp_1024);
            break;
#endif

#if __DIGICERT_MIN_SRP_BITS__ <= 1536
        case 1536: /* 2 */
            *k = srp_1536_k;
            *generator = kGenerators;
            *modulus = srp_1536;
            *modulusLen = sizeof(srp_1536);
            break;
#endif

#if __DIGICERT_MIN_SRP_BITS__ <= 2048
        case 2048: /* 2 */
            *k = srp_2048_k;
            *generator = kGenerators;
            *modulus = srp_2048;
            *modulusLen = sizeof(srp_2048);
            break;
#endif

        case 3072: /* 5 */
            *k = srp_3072_k;
            *generator = kGenerators+1;
#ifdef __ENABLE_DIGICERT_CRYPTO_INTERFACE__
            CRYPTO_INTERFACE_DH_getPByteString( DH_GROUP_15, modulus, modulusLen);
#else
            DH_getPByteString( DH_GROUP_15, modulus, modulusLen);
#endif
            break;

        case 4096: /* 5 */
            *k = srp_4096_k;
           *generator = kGenerators+1;
#ifdef __ENABLE_DIGICERT_CRYPTO_INTERFACE__
            CRYPTO_INTERFACE_DH_getPByteString(DH_GROUP_16, modulus, modulusLen);
#else
            DH_getPByteString(DH_GROUP_16, modulus, modulusLen);
#endif
            break;

        case 6144: /* 5 */
            *k = srp_6144_k;
            *generator = kGenerators+1;
#ifdef __ENABLE_DIGICERT_CRYPTO_INTERFACE__
            CRYPTO_INTERFACE_DH_getPByteString(DH_GROUP_17, modulus, modulusLen);
#else
            DH_getPByteString(DH_GROUP_17, modulus, modulusLen);
#endif
            break;

        case 8192: /* 19 */
            *k = srp_8192_k;
            *generator = kGenerators+2;
#ifdef __ENABLE_DIGICERT_CRYPTO_INTERFACE__
            CRYPTO_INTERFACE_DH_getPByteString(DH_GROUP_18, modulus, modulusLen);
#else
            DH_getPByteString(DH_GROUP_18, modulus, modulusLen);
#endif
            break;

        default:
            status = ERR_INVALID_ARG;
            goto exit;
    }

    *generatorLen = 1;

exit:

    return status;

}


