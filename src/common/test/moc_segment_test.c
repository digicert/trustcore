int test_seg_copy(ubyte4 numSeg, ubyte4 segBufSize, ubyte* pBuf, ubyte4 bufLen, ubyte4 offset)
{
    sbyte4 status;
    mocSegDescr* pBufSeg = NULL;
    int retVal = 0;
    int hint = 0;
    ubyte* tmpBuf = 0;

    mocSegDescr* pNewSeg = NULL;
    ubyte4 newOffset = 0;
    
    tmpBuf = MALLOC(bufLen);
    retVal += UNITTEST_TRUE(0,  0 != tmpBuf);
    if (retVal) goto exit;
   
    retVal += UNITTEST_STATUS( hint++, status = DIGI_createTestSeg(&pBufSeg, numSeg, segBufSize) );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, status = DIGI_copyToSeg(pBuf, bufLen, pBufSeg, offset), bufLen );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, status = DIGI_copyFromSeg(pBufSeg, offset, tmpBuf, bufLen), bufLen );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, memcmp(pBuf, tmpBuf, bufLen), 0);

    memset(tmpBuf, 0x00, bufLen);

    /* copy first half */
    retVal += UNITTEST_INT( hint++, status = DIGI_copyFromSegEx(pBufSeg, offset, tmpBuf, bufLen/2, &pNewSeg, &newOffset), bufLen/2 );
    if (0 > status)  goto exit;

    /* copy second half */
    retVal += UNITTEST_INT( hint++, status = DIGI_copyFromSegEx(pNewSeg, newOffset, tmpBuf + bufLen/2, bufLen - bufLen/2, NULL, NULL), bufLen - bufLen/2 );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, memcmp(pBuf, tmpBuf, bufLen), 0);

    DIGI_clearTestSeg(pBufSeg);

    /* copy first half */
    retVal += UNITTEST_INT( hint++, status = DIGI_copyToSegEx(pBuf, bufLen/2, pBufSeg, offset, &pNewSeg, &newOffset), bufLen/2 );
    if (0 > status)  goto exit;

    /* copy second half */
    retVal += UNITTEST_INT( hint++, status = DIGI_copyToSegEx(pBuf + bufLen/2, bufLen - bufLen/2, pNewSeg, newOffset, NULL, NULL), bufLen - bufLen/2 );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, status = DIGI_copyFromSeg(pBufSeg, offset, tmpBuf, bufLen), bufLen );
    if (0 > status)  goto exit;

    retVal += UNITTEST_INT( hint++, memcmp(pBuf, tmpBuf, bufLen), 0);

    retVal += UNITTEST_STATUS( hint++, status = DIGI_memsetSeg(pBufSeg, 0, MAX_BUFFER_SIZE, 0) );

exit:

    FREE(tmpBuf);
    DIGI_freeTestSeg(&pBufSeg);

    return retVal;
}


/*------------------------------------------------------------------*/

int moc_segment_test_all()
{
    int result = 0;

    result += test_seg_copy(MAX_BUFFER_SIZE, 1, testBuffer, sizeof(testBuffer), 0);

    result += test_seg_copy(MAX_BUFFER_SIZE >> 1, 2, testBuffer, sizeof(testBuffer), 7);

    result += test_seg_copy(1, MAX_BUFFER_SIZE, testBuffer, 1, MAX_BUFFER_SIZE - 1);

    result += test_seg_copy(1, MAX_BUFFER_SIZE, testBuffer, 1, 5);

    return result;
}

