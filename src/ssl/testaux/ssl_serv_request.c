static void
SSL_SERV_REQ_getCipherName( sbyte4 connectionInstance,
                           sbyte** cipherName,
                           ubyte2* cipherId)
{
    sbyte4 i;
    ubyte4 peerECCurves;

    *cipherName = (sbyte*) "internal error";
    if (OK > SSL_getCipherInfo( connectionInstance, cipherId, &peerECCurves))
        return;

    /* linear search */
    for ( i = 0; i < COUNTOF(gCipherDescs); ++i)
    {
        if ( *cipherId == gCipherDescs[i].cipherId)
        {
            *cipherName = (sbyte *)(gCipherDescs[i].cipherName);
            return;
        }
    }

    *cipherName = (sbyte*) "unknown cipher";
}


