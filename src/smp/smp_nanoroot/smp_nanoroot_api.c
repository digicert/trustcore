/* Static pool of NanoROOT_Object pointers */
static NanoROOT_Object* gNanoRootObjects[NanoROOT_MAX_OBJECTS] = {0};

/*
 * Map a key ID to an index in the static pool
 * Returns the index (0-9) if valid, -1 if invalid
 */
static sbyte4 NanoROOT_getIndexFromKeyId(ubyte4 keyId)
{
  switch (keyId)
  {
    case NanoROOT_KEYID_RSA_2K:   return 0;
    case NanoROOT_KEYID_RSA_3K:   return 1;
    case NanoROOT_KEYID_RSA_4K:   return 2;
    case NanoROOT_KEYID_RSA_8K:   return 3;
    case NanoROOT_KEYID_MLDSA44:  return 4;
    case NanoROOT_KEYID_MLDSA65:  return 5;
    case NanoROOT_KEYID_MLDSA87:  return 6;
    case NanoROOT_KEYID_P256:     return 7;
    case NanoROOT_KEYID_P384:     return 8;
    case NanoROOT_KEYID_P521:     return 9;
    default:                     return -1;
  }
}

/*
 * Extract key ID from object ID buffer (little-endian)
 */
static ubyte4 NanoROOT_extractKeyId(const TAP_Buffer *pObjectId)
{
  const ubyte *pIdBytes;

  if (NULL == pObjectId || NULL == pObjectId->pBuffer ||
      pObjectId->bufferLen < sizeof(ubyte4))
  {
    return 0;
  }

  pIdBytes = (const ubyte *)pObjectId->pBuffer;
  /* Big-endian interpretation (network byte order) */
  return ((ubyte4)pIdBytes[0] << 24)  |
         ((ubyte4)pIdBytes[1] << 16)  |
         ((ubyte4)pIdBytes[2] << 8)   |
         ((ubyte4)pIdBytes[3]);
}

/*
 * Find an existing object by ID in the static pool
 * Returns pointer to object if found, NULL otherwise
 */
static NanoROOT_Object* NanoROOT_findObjectById(const TAP_Buffer *pObjectId)
{
  ubyte4 keyId;
  sbyte4 index;

  keyId = NanoROOT_extractKeyId(pObjectId);
  if (0 == keyId)
  {
    return NULL;
  }

  index = NanoROOT_getIndexFromKeyId(keyId);
  if (index < 0)
  {
    return NULL;
  }

  return gNanoRootObjects[index];
}

/*
 * Allocate a new object in the static pool at the index for the given key ID
 * Returns pointer to newly allocated object, NULL if invalid ID or slot occupied
 */
static NanoROOT_Object* NanoROOT_allocateObject(const TAP_Buffer *pObjectId)
{
  ubyte4 keyId;
  sbyte4 index;
  NanoROOT_Object* pNewObject = NULL;

  keyId = NanoROOT_extractKeyId(pObjectId);
  if (0 == keyId)
  {
    return NULL;
  }

  index = NanoROOT_getIndexFromKeyId(keyId);
  if (index < 0)
  {
    DB_PRINT("Invalid key ID 0x%08X - not a supported NanoROOT key\n", keyId);
    return NULL;
  }

  if (gNanoRootObjects[index] != NULL)
  {
    /* Slot already occupied - should not happen if findObjectById was called first */
    return NULL;
  }

  if (OK == DIGI_CALLOC((void**)&pNewObject, 1, sizeof(NanoROOT_Object)))
  {
    gNanoRootObjects[index] = pNewObject;
    return pNewObject;
  }

  return NULL;
}

/*
 * Free an object and remove it from the static pool
 */
static void NanoROOT_freeObject(NanoROOT_Object* pObject)
{
  ubyte4 keyId;
  sbyte4 index;

  if (NULL == pObject)
  {
    return;
  }

  keyId = NanoROOT_extractKeyId(&pObject->objectId);
  index = NanoROOT_getIndexFromKeyId(keyId);

  if (index >= 0 && gNanoRootObjects[index] == pObject)
  {
    (void) CRYPTO_uninitAsymmetricKey(&pObject->privKey, NULL);
    (void) DIGI_MEMSET_FREE(&pObject->objectId.pBuffer, pObject->objectId.bufferLen);
    pObject->objectId.bufferLen = 0;
    pObject->refCount = 0;
    (void) DIGI_FREE((void**)&gNanoRootObjects[index]);
    gNanoRootObjects[index] = NULL;
  }
}

/*
 * Free all objects in the static pool
 */
static void NanoROOT_freeAllObjects(void)
{
  ubyte4 i;
  NanoROOT_Object* pObject;

  for (i = 0; i < NanoROOT_MAX_OBJECTS; i++)
  {
    pObject = gNanoRootObjects[i];
    if (pObject != NULL)
    {
      (void) CRYPTO_uninitAsymmetricKey(&pObject->privKey, NULL);
      (void) DIGI_MEMSET_FREE(&pObject->objectId.pBuffer, pObject->objectId.bufferLen);
      pObject->objectId.bufferLen = 0;
      pObject->refCount = 0;
      (void) DIGI_FREE((void**)&gNanoRootObjects[i]);
      gNanoRootObjects[i] = NULL;
    }
  }
}


static MSTATUS NanoROOT_getCredential(TAP_SealAttributes *pRequestTemplate, ubyte *pCredBuf, ubyte4 *pCredLen)
{
    MSTATUS status = OK;
    ubyte4 count = 0;
    TAP_Attribute *pAttr = NULL;
    TAP_Credential *pCredential = NULL;

    if(NULL == pRequestTemplate || NULL == pCredBuf || NULL == pCredLen)
    {
        return ERR_NULL_POINTER;
    }

    if(NanoROOTMAX_SEED_LEN < *pCredLen)
    {
        return ERR_INTERNAL_ERROR;
    }

    if(((0 < pRequestTemplate->listLen) && (pRequestTemplate->pAttributeList == NULL)) ||
                ((0 == pRequestTemplate->listLen) && (pRequestTemplate->pAttributeList)))
    {
        status = ERR_INVALID_ARG;
        DB_PRINT("%s.%d Error invalid pRequestTemplate. status=%d\n", __FUNCTION__,__LINE__,status);
        goto exit;
    }

    for(count = 0; count < pRequestTemplate->listLen; count++)
    {
        pAttr = &pRequestTemplate->pAttributeList[count];
        if (TAP_ATTR_CREDENTIAL == pAttr->type)
        {
            if(sizeof(TAP_Credential) != pAttr->length)
            {
                status = ERR_INVALID_INPUT;
                DB_PRINT("%s.%d Error invalid pRequestTemplate. status=%d\n", __FUNCTION__,__LINE__,status);
                goto exit;
            }
            if(NULL == pAttr->pStructOfType)
            {
                status = ERR_INVALID_INPUT;
                DB_PRINT("%s.%d Error NULL pStructOfType. status=%d\n", __FUNCTION__,__LINE__,status);
                goto exit;
            }
            pCredential = pAttr->pStructOfType;
            if(pCredential->credentialType == TAP_CREDENTIAL_TYPE_PASSWORD &&
                pCredential->credentialContext == TAP_CREDENTIAL_CONTEXT_USER &&
                pCredential->credentialData.pBuffer &&
                pCredential->credentialData.bufferLen)
            {
                if(pCredential->credentialData.bufferLen > NanoROOTMAX_SEED_LEN)
                {
                    status = ERR_INVALID_INPUT;
                    DB_PRINT("%s.%d Error invalid pRequestTemplate. status=%d\n", __FUNCTION__,__LINE__,status);
                    goto exit;
                }
                *pCredLen = pCredential->credentialData.bufferLen;
                DIGI_MEMCPY(pCredBuf, pCredential->credentialData.pBuffer, pCredential->credentialData.bufferLen);
            }
            else
            {
                status = ERR_INVALID_INPUT;
                DB_PRINT("%s.%d Error invalid pRequestTemplate. status=%d\n", __FUNCTION__,__LINE__,status);
                goto exit;
            }
        }
    }

exit:
    return status;
}


static MSTATUS NanoROOT_genRSAKeyPair(   randomContext *pRandCtx,
                                AsymmetricKey *pPrivKey,
                                ubyte4 ulBitlength
                            )
{
#ifdef __NANOROOT_USE_HARDCODED_TEST_KEY__
    MSTATUS status = OK;
    const ubyte *pKeyDer = NULL;
    ubyte4 keyDerLen = 0;
    const char *keyDesc = NULL;

    MOC_UNUSED(pRandCtx);

    switch(ulBitlength)
    {
        case 3072:
            pKeyDer = gTestRsa3kPrivKeyDer;
            keyDerLen = gTestRsa3kPrivKeyDerLen;
            keyDesc = "RSA-3072";
            break;
        case 4096:
            pKeyDer = gTestRsa4kPrivKeyDer;
            keyDerLen = gTestRsa4kPrivKeyDerLen;
            keyDesc = "RSA-4096";
            break;
        case 8192:
            pKeyDer = gTestRsa8kPrivKeyDer;
            keyDerLen = gTestRsa8kPrivKeyDerLen;
            keyDesc = "RSA-8192";
            break;
        default:
            /* RSA-2048 is default */
            pKeyDer = gTestRsa2kPrivKeyDer;
            keyDerLen = gTestRsa2kPrivKeyDerLen;
            keyDesc = "RSA-2048";
            break;
    }
    status = NanoROOT_loadTestKeyFromDer(pPrivKey, pKeyDer, keyDerLen, keyDesc);
    if (OK != status)
    {
        DB_PRINT("%s.%d Test RSA key load failed, status = %d\n",
                __FUNCTION__, __LINE__, status);
    }
    return status;
#else /* !__NANOROOT_USE_HARDCODED_TEST_KEY__ */
    MSTATUS status = OK;
    vlong *pVlongQueue = NULL;

    DB_PRINT("Create RSA key\n");
    /* Internally invokes CRYPTO_INTERFACE_RSA_createKeyAux() */
    status = CRYPTO_createRSAKey(pPrivKey, NULL);
    if (OK != status)
    {
        DB_PRINT("%s.%d Create RSA Key failed = %d\n",
                 __FUNCTION__, __LINE__, status);
        goto exit;
    }

    DB_PRINT("Generate RSA key of size : %d\n", ulBitlength);
    status = CRYPTO_INTERFACE_RSA_generateKey (pRandCtx, pPrivKey->key.pRSA, ulBitlength, &pVlongQueue);
    if (OK != status)
    {
        DB_PRINT("%s.%d Generate RSA Key failed = %d\n",
                 __FUNCTION__, __LINE__, status);
        goto exit;
    }

exit:
    /* Free any vlongs remaining in the queue */
    VLONG_freeVlongQueue(&pVlongQueue);
    return status;
#endif /* __NANOROOT_USE_HARDCODED_TEST_KEY__ */
}

