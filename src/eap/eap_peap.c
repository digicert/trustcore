/*! Create and initialize an EAP-PEAP session.
This function creates and initializes an EAP-PEAP session based on the specified
parameters, returning the resultant session handle through the $eapPEAPSession$
parameter.

\since 1.41
\version 1.41 and later

! Flags
To enable this function, the following flag must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEAP__$

Additionally, at least one of the following flags must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEER__$
- $__ENABLE_DIGICERT_EAP_AUTH__$

#Include %file:#&nbsp;&nbsp;eap_peap.h

\param appSessionCB     Application session handle (cookie given by the application to identify the session).
\param eapPEAPSession   On return, pointer to EAP-PEAP session handle.
\param eapPEAPparams    Pointer to desired PEAP session parameters.


\return $OK$ (0) if successful; otherwise a negative number error code
definition from merrors.h. To retrieve a string containing an English text
error identifier corresponding to the function's returned error status, use the
$DISPLAY_ERROR$ macro.

*/
extern MSTATUS
EAP_PEAPinitSession(ubyte *appSessionCB,ubyte  **eapPEAPSession, EAP_PEAP_params *eapPEAPparams)
{

    eapPEAPCB* sessionCB = NULL;
    MSTATUS    status = OK;

    if ((NULL == eapPEAPparams) ||
        (NULL == eapPEAPparams->ulTransmit) ||
        (NULL == eapPEAPparams->tls_con) ||
        (NULL == eapPEAPparams->ul2ndStageReceive))
    {
        status = ERR_EAP_PEAP_MISSING_PARAMS;
        goto exit;
    }

    if ((EAP_SESSION_TYPE_PEER != eapPEAPparams->sessionType) &&
        (EAP_SESSION_TYPE_AUTHENTICATOR != eapPEAPparams->sessionType))
    {
        status = ERR_EAP_PEAP_MISSING_PARAMS;
        goto exit;
    }

    if (EAP_SESSION_TYPE_PEER == eapPEAPparams->sessionType)
    {
        if (0 == eapPEAPparams->UserNameLen)
        {
            status = ERR_EAP_PEAP_MISSING_PARAMS;
            goto exit;
        }
    }

    sessionCB = MALLOC(sizeof(eapPEAPCB));

    if (NULL == sessionCB)
    {
        status = ERR_MEM_ALLOC_FAIL;
        goto exit;
    }

    DIGI_MEMSET((ubyte *)sessionCB,0,sizeof(eapPEAPCB));

    DIGI_MEMCPY((ubyte *)&sessionCB->eapPEAPparam,(ubyte *)eapPEAPparams,
               sizeof(EAP_PEAP_params));

    status = EAP_TLSgetMTU(eapPEAPparams->tls_con, (ubyte *)&sessionCB->eapMTU);

    if(OK > status)
        goto exit;


    *eapPEAPSession = (void *)sessionCB;
    sessionCB->appSessionCB = appSessionCB;

exit:
    if (OK > status)
    {
        if (sessionCB)
        {
            FREE(sessionCB);
            sessionCB = NULL;
        }
    }

    return status;
}


