/*! Process an encrypted PEAP payload.
This function processes an encrypted PEAP payload (in the form of
TLVs&mdash;type-length-values), performs any required fragmentation, and passes the
packet to the second phase of the PEAP stack.

\since 1.41
\version 1.41 and later

! Flags
To enable this function, the following flag must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEAP__$

Additionally, at least one of the following flags must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEER__$
- $__ENABLE_DIGICERT_EAP_AUTH__$

#Include %file:#&nbsp;&nbsp;eap_peap.h

\param eapPEAPCb    EAP-PEAP session handle returned from EAP_PEAPinitSession.
\param pkt          Pointer to encrypted PEAP packet.
\param pktLen       Number of bytes of encrypted PEAP data ($pkt$).

\return $OK$ (0) if successful; otherwise a negative number error code
definition from merrors.h. To retrieve a string containing an English text
error identifier corresponding to the function's returned error status, use the
$DISPLAY_ERROR$ macro.

*/
extern MSTATUS
EAP_PEAPreceiveLLPacket(void * eapPEAPCb,ubyte *pkt,ubyte4 pktLen)
{
    eapPEAPCB* tlscon = (eapPEAPCB *)eapPEAPCb;
    MSTATUS    status = OK;
    ubyte*     eap_data = NULL;
    ubyte*     eapRespData;
    ubyte4     eapRespLen;
    ubyte*     eapClearRespData = NULL;
    ubyte4     eapClearRespLen =0;
    ubyte*     eapRemData = NULL;
    ubyte4     eapRemLen;
    /*intBoolean isTlsPkt = FALSE;*/
    ubyte      tlsType = 0;

    if (!tlscon)
    {
        status = ERR_EAP_TLS_SESSION_NOT_FOUND;
        goto exit;
    }
    /* We can recv 3 kinds of messages */
    /* Regular Message with L Flag */
    /* Frag Message with L & M Flag */
    /* Frag Message with  M Flag */
    /* Last Frag Message with  No Flag */
    /* Frag ACK Message Len ==1   */

    /*Initial Packet Generated by the PEAP App  */
    if (0 == pktLen)
        goto process;

    /*P: Verify Version before any other validation*/
    if (tlscon->eapPEAPparam.version != (*(pkt+1) & EAP_TLS_VERSION_MASK))
    {
        status = ERR_EAP_TLS_INVALID_VERSION;
        goto exit;
    }
    if (0 == tlscon->peap_frag_flag)
    {
        /* its an ACK or Other side has recd full pkt . */
        if (1 == pktLen || 2 == pktLen)
        {
            /* Here if we are Auth this must be the Cue for us to
               Start the 2nd Stage EAP BY initing the Stack and Send IdReq
            */

#if (defined(__ENABLE_DIGICERT_EAP_AUTH__))
            if (EAP_SESSION_TYPE_AUTHENTICATOR == tlscon->eapPEAPparam.sessionType)
                status = EAP_PEAPauthCreateSession(tlscon);
#endif

            goto exit;
        }

        if (!(*(pkt+1) & EAP_TLS_LENGTH_FLAG))
        {
            tlscon->peap_data_recv_total_len = pktLen - 2;
        }
        else
        {
            /* Copy The length Over */
            DIGI_MEMCPY((ubyte *) &tlscon->peap_data_recv_total_len, pkt+2, 4);
            tlscon->peap_data_recv_total_len = EAP_NTOHL(tlscon->peap_data_recv_total_len);
        }

        if ((*(pkt+1) & EAP_TLS_MORE_FLAG))
        {
           /* Move it to the buffer */
           tlscon->peap_data_recv = MALLOC(tlscon->peap_data_recv_total_len);

           if (NULL == tlscon->peap_data_recv)
           {
               status = ERR_MEM_ALLOC_FAIL;
               goto exit;
           }

           DIGI_MEMCPY(tlscon->peap_data_recv,pkt+6,pktLen-6);
           tlscon->peap_data_recv_len = pktLen - 6;
           tlscon->peap_frag_flag =  EAP_PEAP_FRAG_FLAG_RECV;
           /* Send ACK */
           /*Send ACK  1 Byte with Version*/
           eap_data = MALLOC(1);
           if (!eap_data)
           {
               status = ERR_MEM_ALLOC_FAIL;
               goto exit;
           }
           *eap_data = tlscon->eapPEAPparam.version;
           tlscon->eapPEAPparam.ulTransmit(tlscon->appSessionCB,eap_data,1,TRUE);
           goto exit;
        }
    }
    else
    {
        if (1 == pktLen || 2 == pktLen) /*its an ACK.. Send Pending Bytes */
        {
            if (EAP_PEAP_FRAG_FLAG_SEND == tlscon->peap_frag_flag)
            {
                status = eap_PEAPsendPendingBytes(tlscon,
                  &eapRespData, &eapRespLen);
                if (OK > status)
                    goto exit;
                tlscon->eapPEAPparam.ulTransmit(tlscon->appSessionCB,
                                                eapRespData,eapRespLen,TRUE);
                goto exit;
            }
            else
            {
                /* getting an ACK when we've already sent all the data.? */
                 status = ERR_EAP_TLS_NO_DATA_TO_SEND;
                 goto exit;
            }
        }
        else
        {
            if (EAP_PEAP_FRAG_FLAG_RECV == tlscon->peap_frag_flag)
            { /* coallese packets..  */
                if (pktLen + tlscon->peap_data_recv_len >= tlscon->peap_data_recv_total_len)
                {
                    /*Should be the last Fragment */
                    DIGI_MEMCPY(tlscon->peap_data_recv+tlscon->peap_data_recv_len,
                           pkt+1,pktLen-1);
                    tlscon->peap_data_recv_len += pktLen - 1;
                    tlscon->peap_frag_flag = 0;
                }
                else
                {
                    /* Should have the more flag set */
                    DIGI_MEMCPY(tlscon->peap_data_recv+tlscon->peap_data_recv_len,
                           pkt+2,pktLen-2);
                    tlscon->peap_data_recv_len += pktLen - 2;
                    /*Send ACK  1 Byte with Version*/
                    eap_data = MALLOC(1);
                    if (!eap_data)
                    {
                        status = ERR_MEM_ALLOC_FAIL;
                        goto exit;
                    }
                    *eap_data = tlscon->eapPEAPparam.version;
                    tlscon->eapPEAPparam.ulTransmit(tlscon->appSessionCB,eap_data,1,TRUE);
                    goto exit;
                }
            }
        }
    }

    if ((tlscon->peap_data_recv) && (tlscon->peap_data_recv_total_len))
    {
        /* Check to See whether a SSL APPLICATION or Handshake/Alert */
        tlsType = *tlscon->peap_data_recv;
        /*if (tlsType != SSL_APPLICATION_DATA)
            isTlsPkt = TRUE;*/
      /* First Decrypt the data packet */
        status = EAP_TLSRecvData((ubyte *)tlscon, tlscon->eapPEAPparam.tls_con,
                                 tlscon->peap_data_recv,
                                 tlscon->peap_data_recv_total_len,
                                 &eapClearRespData, &eapClearRespLen,
                                 &eapRemData, &eapRemLen);

        FREE(tlscon->peap_data_recv);
        tlscon->peap_data_recv = NULL;
        tlscon->peap_data_recv_total_len = 0;
        tlscon->peap_data_recv_len = 0;

        if (OK > status)
            goto exit;
    }
    else
    {
        if ((pkt) && (pktLen))
        {
            if (!(*(pkt+1) & EAP_TLS_LENGTH_FLAG))
            {
                tlsType = *(pkt+2);

                /*if (tlsType != SSL_APPLICATION_DATA)
                    isTlsPkt = TRUE;*/

                /* Data Starts an Offset 2 (PEAPByte,Flag Byte */
                status = EAP_TLSRecvData((ubyte *)tlscon,
                                     tlscon->eapPEAPparam.tls_con,
                                     pkt+2,pktLen-2,
                                     &eapClearRespData, &eapClearRespLen,
                                     &eapRemData, &eapRemLen);
            }
            else
            {
                /* Data Starts an Offset 6 (PEAPByte,Flag Byte,Length 4 Bytes */
                tlsType = *(pkt+6);

                /*if (tlsType != SSL_APPLICATION_DATA)
                    isTlsPkt = TRUE;*/

                status = EAP_TLSRecvData((ubyte *)tlscon,
                                     tlscon->eapPEAPparam.tls_con,
                                     pkt+6,pktLen -6,
                                     &eapClearRespData, &eapClearRespLen,
                                     &eapRemData, &eapRemLen);
            }
            if (OK > status)
                goto exit;

        }
    }

process:

    if (SSL_ALERT == tlsType)
    {
        sbyte4  alertClass;
        /*sbyte4  alertId;*/

        if (2 != eapClearRespLen)
        {
            status = ERR_SSL_PROTOCOL_BAD_LENGTH;
            goto exit;
        }

        if (NULL == eapClearRespData)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }

        alertClass = eapClearRespData[0];
        /*alertId    = eapClearRespData[1];*/

        if ( SSLALERTLEVEL_WARNING  == alertClass )
            status = ERR_SSL_WARNING_ALERT;
        else
            status = ERR_SSL_FATAL_ALERT;

        goto exit;

    }

    if (SSL_HANDSHAKE == tlsType)
    {
      /* Session Resumption */
      /* Inform the App about this  App to harvest data Out from the Send Buffer*/
        status = ERR_EAP_TLS_RECEIVED_HANDSHAKE;
        goto exit;

    }

#if (defined(__ENABLE_DIGICERT_EAP_PEER__))
    /* If Version 0 then directly we get a EAP Packet */
    if ((EAP_SESSION_TYPE_PEER == tlscon->eapPEAPparam.sessionType))
    {

        if (EAP_PEAP_EAP_INIT == tlscon->eapStatus)
        {
            /* Create 2nd Stage EAP Session and  just Send an Ack  */
            status = eap_PEAPPeerInit(eapPEAPCb);
            if (OK > status)
                goto exit;

            tlscon->eapStatus = EAP_PEAP_EAP_IDENTITY;
            eap_data = MALLOC(1);
            if (!eap_data)
            {
                status = ERR_MEM_ALLOC_FAIL;
                goto exit;
            }
            *eap_data = tlscon->eapPEAPparam.version;
            tlscon->eapPEAPparam.ulTransmit(tlscon->appSessionCB,eap_data,1,TRUE);
            goto exit;
        }

        /* Send the Packet Up */
        status = EAP_PEAPProcessEAPRequest(tlscon,eapClearRespData,
                                               eapClearRespLen);
        goto exit;
    }

#endif

#if (defined(__ENABLE_DIGICERT_EAP_AUTH__))
    if ((EAP_SESSION_TYPE_AUTHENTICATOR == tlscon->eapPEAPparam.sessionType))
    {
        if (NULL == tlscon->eapAuthSessionHdl)
        {
            status = EAP_PEAPauthCreateSession(tlscon);
            goto exit;
        }

        /* Send the Packet Up */
        status = EAP_PEAPProcessEAPRequest(tlscon,eapClearRespData,
                                               eapClearRespLen);
        goto exit;
    }
#endif

exit:
    return status;
}


