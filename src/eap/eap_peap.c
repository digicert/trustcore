/* Doc Note: This function is for Mocana internal code use only, and should not
be included in the API documentation.
*/
extern MSTATUS
EAP_PEAPgetTLVbyType(eapPEAPCB *eapCb, ubyte *pPkt, ubyte4 pktLen, ubyte2 type,
                     ubyte2 *pTlvLen, ubyte **pData, ubyte *isMandatory)
{
    ubyte2 tlvType;
    ubyte* ptr = pPkt;
    ubyte2 tlv_len = 0;
    ubyte2 len;
    MSTATUS status = ERR_EAP_TLV_NOT_FOUND;

    *isMandatory = FALSE;
    *pData = NULL;

    while (ptr)
    {
        tlvType = DIGI_NTOHS(ptr) & 0x3fff;
        DIGI_MEMCPY((ubyte *)&len, (ptr + 2), 2);
        len = EAP_NTOHS(len);
        if (tlvType == type)
        {
            if (*ptr & 0x80)
                *isMandatory = TRUE;
            *pTlvLen = len;
            *pData = ptr + 4;
            status = OK;
            break;
        }
        tlv_len += len + 4;
        if (tlv_len < pktLen)
            ptr += tlv_len;
        else
            break;
    }
    return status;
}


