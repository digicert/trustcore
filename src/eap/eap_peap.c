static  MSTATUS
eap_PEAPsendPendingBytes(eapPEAPCB *tlscon,
                        ubyte **eapRespData, ubyte4 *eapRespLen)
{
    MSTATUS status = OK;
    ubyte *eapResponse = NULL;



    DEBUG_PRINTNL(DEBUG_EAP_MESSAGE, (sbyte *) "eap_PEAPsendPendingBytes: got here.");

    if (tlscon->eapMTU >= tlscon->peap_data_send_remaining)
    {
        *eapRespLen = tlscon->peap_data_send_remaining;
        eapResponse = (ubyte *) MALLOC(*eapRespLen);

        if (NULL == eapResponse)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }

        *eapResponse = tlscon->eapPEAPparam.version;
        tlscon->peap_data_send_remaining = 0;
        tlscon->peap_frag_flag = 0;
        FREE(tlscon->peap_data_send);
        tlscon->peap_data_send = NULL;
        tlscon->peap_data_send_cur = NULL;
    }
    else
    {
       /* Will need fragmentation */
        *eapRespLen = tlscon->eapMTU + 1;
        eapResponse = (ubyte *) MALLOC(*eapRespLen);
        if (NULL == eapResponse)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }
        *eapResponse = EAP_TLS_MORE_FLAG | (tlscon->eapPEAPparam.version & EAP_TLS_VERSION_MASK);
        tlscon->peap_data_send_remaining -= tlscon->eapMTU;
    }

    if (tlscon->peap_data_send_remaining)
    {
        status = DIGI_MEMCPY((eapResponse+1),tlscon->peap_data_send_cur,*eapRespLen-1);
        tlscon->peap_data_send_cur+=*eapRespLen - 1;
    }
    else /* Last Fragment */
        status = DIGI_MEMCPY(eapResponse,tlscon->peap_data_send_cur,*eapRespLen);

    *eapRespData = eapResponse;

exit:
    if (OK > status)
    {
        if (eapResponse)
            FREE(eapResponse);
    }

    return status;
}


