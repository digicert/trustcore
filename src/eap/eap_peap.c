/*! Build a PEAP packet.
This function builds a PEAP packet from the specified encrypted second stage
payload, prepending the header and performing any required fragmentation, and
returns the resultant packet through the $eapResponse$ parameter. Typically your
application passes the resulting packet to EAP for transmission from
authenticator to peer or from peer to authenticator.

\since 1.41
\version 1.41 and later

! Flags
To enable this function, the following flag must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEAP__$

Additionally, at least one of the following flags must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEER__$
- $__ENABLE_DIGICERT_EAP_AUTH__$

#Include %file:#&nbsp;&nbsp;eap_peap.h

\param eapPEAPCb    EAP-PEAP session handle returned from EAP_PEAPinitSession.
\param pkt          Pointer to payload to include in the PEAP packet.
\param pktLen       Number of bytes of payload data ($pkt$).
\param eapResponse  On return, pointer to resultant PEAP response packet.
\param eapRespLen   On return, number of bytes in EAP response payload ($eapResponse$).

\return $OK$ (0) if successful; otherwise a negative number error code
definition from merrors.h. To retrieve a string containing an English text
error identifier corresponding to the function's returned error status, use the
$DISPLAY_ERROR$ macro.

*/
extern MSTATUS
EAP_PEAPFormSendPacket(void *eapPEAPCb,ubyte *pkt, ubyte4 pktLen,
                             ubyte **eapResponse, ubyte4 *eapRespLen)
{

    eapPEAPCB* tlscon = (eapPEAPCB *)eapPEAPCb;
    ubyte4     length;
    ubyte*     resp = NULL;
    MSTATUS    status = OK;

    if (!tlscon)
    {
        status = ERR_EAP_TLS_SESSION_NOT_FOUND;
        goto exit;
    }
    /*Create the PEAP Header with L or M bytes */

    *eapResponse = NULL;
    *eapRespLen =  0;

    /* No Need to send L Bit If the Second Stage payload does not need to
       to be fragmented . MS IAS does not like this */
    if (tlscon->eapMTU >= (pktLen + 1))
    {
        *eapRespLen = pktLen + 1;
        resp = MALLOC(*eapRespLen);

        if (NULL == resp)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }

        *resp = (tlscon->eapPEAPparam.version & EAP_TLS_VERSION_MASK);
        tlscon->peap_data_send_remaining = 0;
        DIGI_MEMCPY((ubyte *)(resp +1),(ubyte *)pkt,pktLen);
    }
    else
    {
       /* Will need fragmentation */
        *eapRespLen = tlscon->eapMTU + 5;
        resp = (ubyte *) MALLOC(*eapRespLen);
        if (NULL == resp)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }

        *resp = EAP_TLS_LENGTH_FLAG | EAP_TLS_MORE_FLAG | (tlscon->eapPEAPparam.version & EAP_TLS_VERSION_MASK);
        length = EAP_HTONL(pktLen);
        DIGI_MEMCPY(resp +1,(ubyte *)&length,4);
        tlscon->peap_data_send_remaining =pktLen - tlscon->eapMTU;
        tlscon->peap_frag_flag = EAP_PEAP_FRAG_FLAG_SEND;

        DIGI_MEMCPY(resp +5,(ubyte *)pkt+5,tlscon->eapMTU);
        /*Cache the packet for Future */
        tlscon->peap_data_send = pkt;
        tlscon->peap_data_send_cur = pkt+tlscon->eapMTU;
    }

    *eapResponse = resp;

exit:

    return status;

}


