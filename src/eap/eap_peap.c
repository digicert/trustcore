/* Doc Note: This function is for Mocana internal code use only, and should not
be included in the API documentation.
*/
extern  MSTATUS
EAP_PEAP_llTransmitPktCallback(ubyte *appSessionHdl,
                          eapHdr_t *eap_hdr,
                          ubyte *eap_data,
                          ubyte4 eap_data_len)
{
    eapPEAPCB*  eapCb = (eapPEAPCB *) appSessionHdl;
    ubyte*      response = NULL;
    ubyte4      responseLen;
    MSTATUS     status = OK;

    if (NULL == eapCb)
    {
        status = ERR_EAP_INVALID_SESSION;
        goto exit;
    }

    response = MALLOC(eap_data_len);

    if (NULL == response)
    {
        status = ERR_MEM_ALLOC_FAIL;
        goto exit;
    }

    DIGI_MEMCPY(response,eap_data,eap_data_len);
    /* PEAP Version 0 , No Header Sent except for Type TLV */

    if (0 == eapCb->eapPEAPparam.version)
    {
        if ((EAP_CODE_REQUEST == eap_hdr->code) ||
            (EAP_CODE_RESPONSE == eap_hdr->code))
        {
            if (4 < eap_data_len)
            {
               if(EAP_TYPE_TLV == *(eap_data))
               {
                   FREE(response);
                   response = NULL;
                   goto process;
               }

            }
        }
        eapCb->eapPEAPparam.ulTransmit(eapCb->appSessionCB,
                                       response,eap_data_len,FALSE);
        goto exit;

    }

process:
    FREE(response);
    response = NULL;

    response = MALLOC(sizeof(eapHdr_t)+eap_data_len);

    responseLen =  (sizeof(eapHdr_t)+eap_data_len);

    if (NULL == response)
    {
        status = ERR_MEM_ALLOC_FAIL;
        goto exit;
    }

    DIGI_MEMCPY(response,(ubyte *)eap_hdr,sizeof(eapHdr_t));
    DIGI_MEMCPY(response+sizeof(eapHdr_t),eap_data,eap_data_len);

    /*Call First Stage*/
    eapCb->eapPEAPparam.ulTransmit(eapCb->appSessionCB,
                                   response, responseLen, FALSE);

exit:
    return status;
}

