/*! Assign the code and ID values to an inner EAP header.
This function assigns the specified code and ID values to an inner EAP header.
Typically this is used in PEAP v0 when the authenticator or peer has not sent
the inner header information, which is required by the inner (second stage) EAP
state machine for packet processing.

\since 2.02
\version 2.02 and later

! Flags
To enable this function, the following flag must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEAP__$

Additionally, at least one of the following flags must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEER__$
- $__ENABLE_DIGICERT_EAP_AUTH__$

#Include %file:#&nbsp;&nbsp;eap_peap.h

\param eapCB    EAP-PEAP session handle returned from EAP_PEAPinitSession.
\param code     Any of the $eapCode$ enumerated values (defined in eap_proto.h).
\param id       EAP request header ID (unique to this session).

\return $OK$ (0) if successful; otherwise a negative number error code
definition from merrors.h. To retrieve a string containing an English text
error identifier corresponding to the function's returned error status, use the
$DISPLAY_ERROR$ macro.

*/
extern MSTATUS
EAP_PEAPSetEapHdr(void  *eapCB, eapCode code, ubyte id)
{
    eapPEAPCB*  eapCb = (eapPEAPCB *) eapCB;
    MSTATUS status = OK;

    if (NULL == eapCb)
    {
        status = ERR_EAP_INVALID_SESSION;
        goto exit;
    }
    eapCb->recvCode = code;
    eapCb->recvId   = id;

exit:
    return status ;
}

