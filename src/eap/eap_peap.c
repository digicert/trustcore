/* Doc Note: This function is for Mocana internal code use only, and should not
be included in the API documentation.
*/
extern MSTATUS
EAP_PEAPProcessEAPRequest(eapPEAPCB *eapCb, ubyte *pkt, ubyte4 pktLen)
{
   /* Pass the packet up  to the Peer  Second Stage*/
    MSTATUS    status = OK;
    eapHdr_t*  eapHdr;
    ubyte*     newPkt = pkt;

    if ((0 == eapCb->eapPEAPparam.version) ||
        (1 == pktLen) )
    {
        if (NULL == pkt)
        {
            status = ERR_NULL_POINTER;
            goto exit;
        }

        /* If the Type is TLV then the Other Side sends a Full EAP Header */

        if ((EAP_CODE_REQUEST == *pkt)  ||
            (EAP_CODE_RESPONSE == *pkt))
        {
            if (4 < pktLen)
            {
                if (EAP_TYPE_TLV == *(pkt+4))
                    goto process;

            }
        }

    /* Add the EAP Hdr for Peap Version 0 */
        newPkt    = MALLOC(sizeof(eapHdr)+pktLen);
        if (NULL == newPkt)
        {
            status = ERR_MEM_ALLOC_FAIL;
            goto exit;
        }

        eapHdr = (eapHdr_t *)newPkt;

        eapHdr->code = eapCb->recvCode;
        eapHdr->id   = eapCb->recvId;
        /* Set the Length */
        DIGI_HTONS(newPkt + 2,sizeof(eapHdr_t) + pktLen);

        DIGI_MEMCPY(newPkt + sizeof(eapHdr_t),pkt,pktLen);
        pktLen += sizeof(eapHdr_t);
    }

process:


    if ((EAP_SESSION_TYPE_PEER == eapCb->eapPEAPparam.sessionType))
    {
        status = EAP_llReceivePacket(eapCb->eapSessionHdl,
                                     eapCb->eapPEAPparam.instanceId,
                                     newPkt,pktLen,NULL);
    }
    else
    {
        status = EAP_llReceivePacket(eapCb->eapAuthSessionHdl,
                                     eapCb->eapPEAPparam.instanceId,
                                     newPkt,pktLen,NULL);
    }


exit:
    if ((newPkt)  && (newPkt !=pkt))
        FREE(newPkt);

    return status;

}


