/*! Generate and return a session's authentication keys.
This function generates and returns a session's authentication keys.

\since 2.02
\version 2.02 and later

! Flags
To enable this function, the following flag must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEAP__$

Additionally, at least one of the following flags must be defined in moptions.h:
- $__ENABLE_DIGICERT_EAP_PEER__$
- $__ENABLE_DIGICERT_EAP_AUTH__$

#Include %file:#&nbsp;&nbsp;eap_peap.h

\param eapCb        EAP-PEAP session handle returned from EAP_PEAPinitSession.
\param key          On return, pointer to authentication keys.
\param keyLen       Number of bytes desired in the authentication key.

\return $OK$ (0) if successful; otherwise a negative number error code
definition from merrors.h. To retrieve a string containing an English text
error identifier corresponding to the function's returned error status, use the
$DISPLAY_ERROR$ macro.

*/
extern MSTATUS
EAP_PEAPgetKey(void *eapCb,ubyte *key,ubyte2 keyLen/* 128 or 192 Bytes */)
{
   /* Generate key based upon the TLS Expansion Key Algo */
    eapPEAPCB * eapPEAPCb = (eapPEAPCB *) eapCb;
    MSTATUS status;

    if (NULL == eapPEAPCb)
    {
        status = ERR_EAP_INVALID_SESSION;
        goto exit;
    }

    if (eapPEAPCb->eapPEAPparam.version < 1)
        status = SSL_generateTLSExpansionKey(
                                    eapPEAPCb->eapPEAPparam.connectionInstance,
                                    key,keyLen,(ubyte *) PEAP_KEYING_PHRASE_OLD,
                                    PEAP_KEYING_PHRASE_OLD_LEN);
    else
        status = SSL_generateTLSExpansionKey(
                                    eapPEAPCb->eapPEAPparam.connectionInstance,
                                    key,keyLen,(ubyte *) PEAP_KEYING_PHRASE_NEW,
                                    PEAP_KEYING_PHRASE_NEW_LEN);

exit:
    return status;
}

